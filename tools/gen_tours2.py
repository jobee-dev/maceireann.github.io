def build(t):
    stops_html = ''.join(
        f'\n                <li><h3>{html.escape(s[0])}</h3><p>{html.escape(s[1])}</p></li>'
        for s in t['stops']) + '\n            '
    itinerary = ', '.join(
        f'{{ "@type": "ListItem", "position": {i+1}, "item": {{ "@type": "TouristAttraction", "name": "{s[0].replace(chr(34),chr(39))}" }} }}'
        for i, s in enumerate(t['stops']))
    variants_html = ''.join(
        f'\n                <div class="variant"><h3>{html.escape(n)}</h3><span class="variant-meta">{m}</span></div>'
        for n, m in t['variants']) + '\n            '
    vn = t.get('variants_note', '')
    variants_note = f'<p class="variants-note">{vn}</p>' if vn else ''
    related_html = ''.join(
        f'\n                <a class="related-card" href="/tours/{r[0]}.html" style="background-image:url(\'{r[2]}\');"><span>{r[1]}</span></a>'
        for r in t['related']) + '\n            '
    data = {k: v for k, v in t.items() if k not in ('stops','related','variants','variants_note')}
    out = TPL.format(
        app_url=APP_URL, app_badge=APP_BADGE,
        svg_dur=svg('dur'), svg_dist=svg('dist'), svg_dir=svg('dir'), svg_pin=svg('pin'),
        stops_html=stops_html, itinerary=itinerary,
        variants_html=variants_html, variants_note=variants_note,
        related_html=related_html, **data)
    with open(f"tours/{t['slug']}.html", 'w') as f:
        f.write(out)
    print(f"wrote tours/{t['slug']}.html ({len(t['stops'])} stops, {len(t['variants'])} variants)")
